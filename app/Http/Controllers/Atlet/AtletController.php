    {
//        dd($request->file);
        $file = $request->file('file');
        $nameFile = $file->getClientOriginalName();
        $file->move('DataAtlet', $nameFile);

        Excel::import(new AtletImport, public_path('/DataAtlet/'.$nameFile));
        return redirect(route('atlet.add'));
    }

